class DeepSeekManager:
    """
    Единый класс для работы с DeepSeek API.
    Маршрутизация:
      - deepseek-chat:     Форматтер тем, Модератор
      - deepseek-reasoner: Философ/Оппонент, ИИ-Жюри
    
    Контекстное кэширование:
      DeepSeek автоматически кэширует совпадающий префикс промпта (64+ токенов).
      Для оппонента мы накапливаем историю сообщений, чтобы максимизировать cache hits.
    """

    MODEL_CHAT = "deepseek-chat"
    MODEL_REASONER = "deepseek-reasoner"

    def __init__(self):
        from openai import OpenAI
        api_key = os.environ.get('DEEPSEEK_API_KEY')
        if not api_key:
            raise ValueError("DEEPSEEK_API_KEY не найден в переменных окружения!")
        self.client = OpenAI(
            base_url="https://api.deepseek.com",
            api_key=api_key
        )
        self.reset_stats()
        print("[DeepSeek] Клиент инициализирован.")


    def reset_stats(self):
        """Сбрасывает счетчики токенов перед новой игрой."""
        self._stats = {
            'topic_formatter': {'prompt': 0, 'completion': 0, 'cache_hit': 0, 'cache_miss': 0, 'calls': 0},
            'moderator':       {'prompt': 0, 'completion': 0, 'cache_hit': 0, 'cache_miss': 0, 'calls': 0},
            'opponent':        {'prompt': 0, 'completion': 0, 'cache_hit': 0, 'cache_miss': 0, 'calls': 0},
            'jury':            {'prompt': 0, 'completion': 0, 'cache_hit': 0, 'cache_miss': 0, 'calls': 0},
        }
        self.rag_citations = []

    def _track_usage(self, response, stage: str):
        """Извлекает usage из ответа API и суммирует в аккумулятор."""
        if response and hasattr(response, 'usage') and response.usage:
            usage = response.usage
            p = usage.prompt_tokens or 0
            c = usage.completion_tokens or 0
            cache_hit = getattr(usage, 'prompt_cache_hit_tokens', 0) or 0
            cache_miss = getattr(usage, 'prompt_cache_miss_tokens', 0) or 0
            if stage in self._stats:
                self._stats[stage]['prompt'] += p
                self._stats[stage]['completion'] += c
                self._stats[stage]['cache_hit'] += cache_hit
                self._stats[stage]['cache_miss'] += cache_miss
                self._stats[stage]['calls'] += 1
            cache_pct = f" (🟢 cache hit: {cache_hit}/{p})" if cache_hit > 0 else ""
            print(f"[DeepSeek Token] {stage}: +{p} prompt, +{c} completion{cache_pct}")

    def print_game_stats(self):
        """Выводит подробную статистику токенов за раунд дебатов."""
        total_p, total_c, total_hit, total_miss = 0, 0, 0, 0
        print("\n" + "=" * 70)
        print("    📊  СТАТИСТИКА ТОКЕНОВ ЗА РАУНД ДЕБАТОВ")
        print("=" * 70)
        print(f"  {'Роль':20s} | {'Выз':>3s} | {'Prompt':>7s} | {'Compl':>7s} | {'🟢 Cache':>8s} | {'🔴 Miss':>7s}")
        print("-" * 70)
        for stage, data in self._stats.items():
            p, c, n = data['prompt'], data['completion'], data['calls']
            hit, miss = data['cache_hit'], data['cache_miss']
            total_p += p
            total_c += c
            total_hit += hit
            total_miss += miss
            label = {
                'topic_formatter': 'Форматтер тем',
                'moderator': 'Модератор',
                'opponent': 'Оппонент (RAG)',
                'jury': 'Жюри',
            }.get(stage, stage)
            print(f"  {label:20s} | {n:3d} | {p:7d} | {c:7d} | {hit:8d} | {miss:7d}")
        print("-" * 70)
        total = total_p + total_c
        print(f"  {'ИТОГО':20s} |     | {total_p:7d} | {total_c:7d} | {total_hit:8d} | {total_miss:7d}")
        print(f"  Всего токенов: {total}")
        cost_miss   = total_miss / 1_000_000 * 0.55
        cost_hit    = total_hit  / 1_000_000 * 0.14
        cost_output = total_c    / 1_000_000 * 2.19
        total_cost  = cost_miss + cost_hit + cost_output
        cost_no_cache = total_p / 1_000_000 * 0.55 + cost_output
        saved = cost_no_cache - total_cost
        print(f"  💰 Стоимость: ~${total_cost:.4f} (без кэша было бы ~${cost_no_cache:.4f}, экономия: ${saved:.4f})")
        if total_p > 0:
            cache_pct = total_hit / total_p * 100
            print(f"  🗄️  Кэш: {cache_pct:.1f}% промпт-токенов из кэша")
  
        if self.rag_citations:
            print("\n  📚 RAG-цитаты, использованные в игре:")
            for i, cite in enumerate(self.rag_citations, 1):
                short = cite[:120].replace('\n', ' ') + ('...' if len(cite) > 120 else '')
                print(f"    {i}. {short}")
        print("=" * 70 + "\n")

    def log_rag_citation(self, citation_text: str):
        """Сохраняет RAG-цитату для итоговой статистики."""
        if citation_text and citation_text not in self.rag_citations:
            self.rag_citations.append(citation_text)


    def format_topic(self, raw_topic: str) -> str:
        """Превращает сырую тему в строгий академический тезис для дебатов."""
        try:
            response = self.client.chat.completions.create(
                model=self.MODEL_CHAT,
                messages=[
                    {"role": "system", "content": TOPIC_FORMATTER_SYSTEM_PROMPT},
                    {"role": "user",   "content": TOPIC_FORMATTER_PROMPT.format(raw_topic=raw_topic)},
                ],
            )
            self._track_usage(response, 'topic_formatter')

            topic_text = response.choices[0].message.content.strip()
            topic_text = re.sub(r'<think>.*?</think>', '', topic_text, flags=re.DOTALL).strip()
            topic_text = re.sub(r'^["\']|["\']$', '', topic_text).strip()

            print(f"[TopicFormatter] Успех: {topic_text}")
            return topic_text
        except Exception as e:
            print(f"[TopicFormatter] Ошибка DeepSeek: {e} — возвращаем оригинальный ввод.")
            return raw_topic

    def validate_attack_question(self, question: str, opponent_position: str, user_position: str) -> str:
        prompt = (
            "Ты — строгий логический валидатор на дебатах. Твоя единственная цель — предотвратить 'самострел'.\n\n"
            f"ПОЗИЦИЯ АВТОРА ВОПРОСА (Атакующий): {opponent_position}...\n"
            f"ПОЗИЦИЯ ЕГО ПРОТИВНИКА (Защищающийся): {user_position}...\n\n"
            f"ВОПРОС: {question}\n\n"
            "ПРОАНАЛИЗИРУЙ: Этот вопрос атакует позицию противника или случайно признает его правоту/подыгрывает ему?\n"
            "Если вопрос корректно атакует противника — верни его без изменений.\n"
            "Если вопрос подыгрывает противнику (САМОСТРЕЛ) — переформулируй его так, чтобы он жестко бил по позиции противника.\n\n"
            "ВЕРНИ ТОЛЬКО ТЕКСТ ВОПРОСА. НИКАКИХ ПОЯСНЕНИЙ."
        )
        try:
            # Дешевый запрос на валидацию
            response = self.client.chat.completions.create(
                model=self.MODEL_CHAT,
                messages=[{"role": "user", "content": prompt}],
                temperature=0.1
            )
            validated = response.choices[0].message.content.strip()
            self._track_usage(response, 'moderator')
            return validated
        except Exception as e:
            print(f"Validation Error: {e}")
            return question


    def generate_moderator(self, system_prompt: str, user_prompt: str) -> str:
        """Генерирует речь модератора (без стриминга, без истории).
        Каждый вызов — независимый. DeepSeek кэширует system_prompt автоматически."""
        try:
            response = self.client.chat.completions.create(
                model=self.MODEL_CHAT,
                messages=[
                    {"role": "system", "content": system_prompt},
                    {"role": "user",   "content": user_prompt},
                ],
            )
            self._track_usage(response, 'moderator')
            return response.choices[0].message.content.strip()
        except Exception as e:
            print(f"[Moderator] Ошибка DeepSeek: {e}")
            return f"Ошибка при обращении к AI: {e}"


    def generate_opponent(self, system_prompt: str, user_prompt: str, stream: bool = False, use_chat_model: bool = False):
        """Генерирует речь оппонента (без накопления истории).
        debate_manager уже включает стенограмму в промпт,
        поэтому каждый вызов — [system, user]. DeepSeek кэширует system_prompt автоматически."""
        try:
            model_to_use = self.MODEL_CHAT if use_chat_model else self.MODEL_REASONER
            response = self.client.chat.completions.create(
                model=model_to_use,
                messages=[
                    {"role": "system", "content": system_prompt},
                    {"role": "user",   "content": user_prompt},
                ],
                stream=stream,
            )
            if stream:
                return self._stream_wrapper(response, 'opponent')
            else:
                self._track_usage(response, 'opponent')
                return response.choices[0].message.content.strip()
        except Exception as e:
            print(f"[Opponent] Ошибка DeepSeek: {e}")
            if stream:
                return iter([])
            return f"Ошибка при обращении к AI: {e}"


    def generate_jury(self, system_prompt: str, user_prompt: str) -> str:
        """Генерирует вердикт жюри."""
        try:
            response = self.client.chat.completions.create(
                model=self.MODEL_REASONER,
                messages=[
                    {"role": "system", "content": system_prompt},
                    {"role": "user",   "content": user_prompt},
                ],
            )
            self._track_usage(response, 'jury')
            raw = response.choices[0].message.content.strip()
            raw = re.sub(r'<think>.*?</think>', '', raw, flags=re.DOTALL).strip()
            return raw
        except Exception as e:
            print(f"[Jury] Ошибка DeepSeek: {e}")
            return f"Ошибка при обращении к AI: {e}"


    def _stream_wrapper(self, stream_response, stage: str):
        """Обёртка вокруг потокового ответа. Пробрасывает чанки текста и трекает usage."""
        total_prompt = 0
        total_completion = 0
        cache_hit = 0
        cache_miss = 0
        
        for chunk in stream_response:
            if hasattr(chunk, 'usage') and chunk.usage:
                total_prompt = chunk.usage.prompt_tokens or 0
                total_completion = chunk.usage.completion_tokens or 0
                cache_hit = getattr(chunk.usage, 'prompt_cache_hit_tokens', 0) or 0
                cache_miss = getattr(chunk.usage, 'prompt_cache_miss_tokens', 0) or 0
            
            if chunk.choices and chunk.choices[0].delta and chunk.choices[0].delta.content:
                yield chunk.choices[0].delta.content

        if total_prompt or total_completion:
            if stage in self._stats:
                self._stats[stage]['prompt'] += total_prompt
                self._stats[stage]['completion'] += total_completion
                self._stats[stage]['cache_hit'] += cache_hit
                self._stats[stage]['cache_miss'] += cache_miss
                self._stats[stage]['calls'] += 1
            cache_info = f" (🟢 cache hit: {cache_hit}/{total_prompt})" if cache_hit > 0 else ""
            print(f"[DeepSeek Token Stream] {stage}: +{total_prompt} prompt, +{total_completion} completion{cache_info}")




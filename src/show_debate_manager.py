"""
ShowDebateManager — менеджер дебатов для режима «Шоу: ИИ vs ИИ».
Аналог DebateManager, но вместо пользователя на одной стороне — второй ИИ.
"""


class ShowDebateManager:
    def __init__(self, topic, p1_name="Иммануил Кант", p2_name="Сократ", rounds_count=3):
        self.p1_name = p1_name
        self.p2_name = p2_name
        self.topic = topic
        self.rounds_count = rounds_count
        self.transcript = []

        # Позиции, зафиксированные после вступительных речей
        self.p1_declared_position = None
        self.p2_declared_position = None

    def set_positions_after_opening(self, p1_opening: str, p2_opening: str):
        self.p1_declared_position = p1_opening[:300] if p1_opening else "не озвучена"
        self.p2_declared_position = p2_opening[:300] if p2_opening else "не озвучена"

    def _add_to_transcript(self, speaker, text):
        line = f"[{speaker}]: {text}"
        self.transcript.append(line)
        print(f"SHOW_TRANSCRIPT: {line}")

    # ──────────────────────────────────────
    # ФАЗА 1: Вступительное слово
    # ──────────────────────────────────────

    def get_opening_prompt(self, other_speech=""):
        """Промпт для вступительного слова (одинаков для обоих)."""
        instruction = (
            "───── ФАЗА 1: ВСТУПИТЕЛЬНОЕ СЛОВО ─────\n"
            "Это была вступительная речь твоего оппонента (или её не было, "
            "если ты говоришь первым). Произнеси свою вступительную речь, "
            "строго придерживаясь своей роли, личности и темы дебатов."
        )
        return f"[ИНСТРУКЦИЯ]: {instruction}\n[РЕЧЬ ОППОНЕНТА]: {other_speech}"

    # ──────────────────────────────────────
    # ФАЗА 2: Полемика
    # ──────────────────────────────────────

    def _position_anchor_block(self, attacker_name, defender_name) -> str:
        """Блок-напоминание позиций для атакующего."""
        attacker_pos = self.p1_declared_position if attacker_name == self.p1_name else self.p2_declared_position
        defender_pos = self.p2_declared_position if attacker_name == self.p1_name else self.p1_declared_position

        if attacker_pos and attacker_pos != "не озвучена":
            return (
                "⚠️ НАПОМИНАНИЕ ПОЗИЦИЙ:\n"
                f"ТЫ защищаешь: «{attacker_pos[:200]}»\n"
                f"Твой ВРАГ защищает: «{defender_pos[:200]}»\n"
                "Твой вопрос ОБЯЗАН атаковать позицию ВРАГА.\n\n"
            )
        return ""

    def get_clash_leader_prompt(self, last_responder_speech, attacker_name, defender_name):
        """Промпт для атакующего (задаёт вопрос)."""
        anchor = self._position_anchor_block(attacker_name, defender_name)
        instruction = (
            f"{anchor}\n"
            "───── ФАЗА 2: РАУНД ПОЛЕМИКИ ─────\n"
            ">> РЕЖИМ АТАКИ (ты задаёшь вопросы):\n"
            "ТЫ ВЕДУЩИЙ этого раунда. Твоя задача — нанести точечный логический удар по позиции противника.\n"
            "ПРАВИЛО АНТИ-ХАМЕЛЕОН: Твой вопрос должен исходить СТРОГО из ТВОЕЙ позиции. "
            "ЗАПРЕЩЕНО задавать вопросы, которые звучат так, будто ты соглашаешься с оппонентом или сомневаешься в своей правоте. "
            "Не поддакивай его логике!\n\n"
            "🚫 ОБЯЗАТЕЛЬНАЯ САМОПРОВЕРКА (3 шага)\n"
            "Шаг 1: Сформулируй вопрос\n"
            "Шаг 2: Представь что оппонент блестяще ответил — это усиливает ЕГО позицию или ТВОЮ?\n"
            "Шаг 3: Если усиливает ЕГО → САМОСТРЕЛ → удали и придумай другой\n\n"
            "ЗАДАЙ РОВНО ОДИН, ОЧЕНЬ КОРОТКИЙ (20-25 слов) И КАВЕРЗНЫЙ ВОПРОС, "
            "который вскрывает слабость его последней реплики.\n"
            "ВЫВЕДИ ТОЛЬКО ВОПРОС. Без преамбулы, без рассуждений."
        )
        return f"[ИНСТРУКЦИЯ]: {instruction}\n[РЕПЛИКА ОППОНЕНТА ДЛЯ АТАКИ]: {last_responder_speech}"

    def get_clash_responder_prompt(self, last_leader_speech):
        """Промпт для защищающегося (отвечает на вопрос)."""
        instruction = (
            "───── ФАЗА 2: РАУНД ПОЛЕМИКИ ─────\n"
            ">> РЕЖИМ ЗАЩИТЫ (тебе задают вопросы):\n"
            "ТЫ ОТВЕЧАЮЩИЙ. Оппонент пытается загнать тебя в ловушку.\n"
            "ЖЕСТКИЙ ЗАПРЕТ: НИКОГДА не соглашайся с его доводами, не иди на компромисс и не признавай его правоту. "
            "Твоя задача — КРАТКО (до 50 слов) отбить атаку, переосмыслить его аргумент в свою пользу и защитить свою позицию.\n"
            "ЗАПРЕЩЕНО задавать встречные вопросы — только уверенный, железобетонный ответ."
        )
        return f"[ИНСТРУКЦИЯ]: {instruction}\n[АТАКА ОППОНЕНТА (ОТБЕЙ ЕЁ)]: {last_leader_speech}"

    # ──────────────────────────────────────
    # ФАЗА 3: Вопросы от жюри
    # ──────────────────────────────────────

    def get_jury_questions_prompt(self):
        transcript_str = "\n".join(self.transcript)
        return f"""
        Проанализируй стенограмму дебатов и сгенерируй по одному вопросу каждому участнику.
        Участники: {self.p1_name} и {self.p2_name}.
        Твой ответ ДОЛЖЕН БЫТЬ в строгом формате JSON с ключами "question_for_p1" и "question_for_p2".

        --- СТЕНОГРАММА ---
        {transcript_str}
        """

    def get_jury_answer_prompt(self, jury_question):
        """Промпт для ИИ-оппонента, отвечающего на вопрос жюри."""
        instruction = (
            "───── ФАЗА 3: ВОПРОС ОТ ЖЮРИ ─────\n"
            "Коллегия жюри задала тебе вопрос. Ответь на него четко и по существу, "
            "оставаясь в своей роли. Начни ответ, обращаясь к жюри."
        )
        return f"[ИНСТРУКЦИЯ]: {instruction}\n[ВОПРОС ОТ ЖЮРИ]: {jury_question}"

    # ──────────────────────────────────────
    # ФАЗА 4: Заключительное слово
    # ──────────────────────────────────────

    def get_summary_prompt(self):
        instruction = (
            "───── ФАЗА 4: ЗАКЛЮЧИТЕЛЬНОЕ СЛОВО ─────\n"
            "Произнеси свою заключительную речь. Тебе нужно кратко подвести итоги "
            "и финальный раз убедить жюри в своей правоте. Не вводи новых аргументов."
        )
        return f"[ИНСТРУКЦИЯ]: {instruction}"

    # ──────────────────────────────────────
    # ФИНАЛ: Вердикт жюри (3M)
    # ──────────────────────────────────────

    def get_3m_verdict_prompt(self):
        transcript_str = "\n".join(self.transcript)
        return f"""
 Ты — беспристрастное независимое ИИ-жюри. Тебе предоставлена стенограмма дебатов между {self.p1_name} и {self.p2_name}. Твоя задача — определить победителя, строго опираясь на международный стандарт оценки дебатов "Правило 3M" (Matter, Manner, Method).

КРИТЕРИИ ОЦЕНКИ (Макс 100 баллов на участника):

1. MATTER / Содержание (Макс 40 баллов):
  - 1a. Аргументация (0-10): Сила, логичность и доказанность тезисов.
  - 1b. Полемика (Clash) (0-15): Качество контраргументации и способность отбивать атаки.
  - 1c. Качество ответов (0-10): Глубина и точность ответов на прямые вопросы.
  - 1d. Последовательность (Consistency) (0-5): Отсутствие логических дыр и противоречий самому себе.

2. MANNER / Подача (Макс 20 баллов):
  - 2a. Риторические приёмы (0-10): Использование риторических инструментов.
  - 2b. Лексическое разнообразие (0-10): Богатство языка, словарный запас.

3. METHOD / Стратегия (Макс 40 баллов):
  - 3a. Coherence / Связность (0-10): Четкость структуры ответов.
  - 3b. Targeting / Фокус (0-10): Умение бить в суть проблемы оппонента.
  - 3c. Качество вопросов в полемике (0-20): Насколько каверзными и эффективными были вопросы.

ФОРМАТ ОТВЕТА:
Ты ОБЯЗАН вернуть ответ ИСКЛЮЧИТЕЛЬНО в формате валидного JSON без markdown-разметки.
НЕ считай суммы — выставляй ТОЛЬКО подкритерии. Суммы будут посчитаны программно.

ВАЖНО: в поле "winner" напиши ТОЧНОЕ имя победителя ({self.p1_name} или {self.p2_name}) или "Ничья".

Структура JSON:
{{
  "p1_scores": {{
    "matter_argumentation": 0,
    "matter_clash": 0,
    "matter_answers": 0,
    "matter_consistency": 0,
    "manner_rhetoric": 0,
    "manner_language": 0,
    "method_coherence": 0,
    "method_targeting": 0,
    "method_questions": 0
  }},
  "p2_scores": {{
    "matter_argumentation": 0,
    "matter_clash": 0,
    "matter_answers": 0,
    "matter_consistency": 0,
    "manner_rhetoric": 0,
    "manner_language": 0,
    "method_coherence": 0,
    "method_targeting": 0,
    "method_questions": 0
  }},
  "explanation_matter": "Обоснование оценок по блоку Содержание",
  "explanation_manner": "Обоснование оценок по блоку Подача",
  "explanation_method": "Обоснование оценок по блоку Стратегия",
  "winner": "{self.p1_name} / {self.p2_name} / Ничья",
  "verdict_explanation": "Общее итоговое обоснование победителя"
}}

--- СТЕНОГРАММА ---
{transcript_str}
"""

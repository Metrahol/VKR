import os
import sys
import time

# Добавляем папку src в пути, чтобы правильно импортировать модули
sys.path.insert(0, os.path.abspath(os.path.join(os.path.dirname(__file__), 'src')))

from agents import DeepSeekManager, get_opponent_system_prompt

def main():
    deepseek = DeepSeekManager()

    topic = "Антиинтеллектуализм: угроза прогрессу или здоровая реакция на элитизм?"
    opponent_name = "Альберт Эйнштейн"

    user_position = """Я выступаю за антиинтеллектуализм, потому что это единственный способ вернуть человека к реальности. Интеллектуал смотрит на мир через призму чужих концепций, графиков и терминов. Он пытается упаковать живую, хаотичную и прекрасную жизнь в сухие формулы."""

    opponent_position = """Я защищаю интеллектуализм, потому что это единственный способ увидеть мир не как случайного наблюдателя, а как творца, способного понять саму ткань пространства-времени. Именно интеллект, работая с абстракциями, открывает структуры реальности, невидимые глазу."""

    last_user_speech = """Скажите, как вы считаете: может ли человек, который прочитал 1000 книг о любви, но ни разу не любил, или изучил сотни томов по психологии горя, но никогда ничего не терял, претендовать на то, что он знает и понимает эти явления лучше, чем тот, кто просто прожил их? Не кажется ли вам, что интеллектуализм — это попытка съесть меню вместо самого обеда?"""

    system_prompt = get_opponent_system_prompt(topic, opponent_name)

    anchor = (
        "\n══════════════════════════════════════\n"
        "⚠️  ЯКОРЬ ПОЗИЦИЙ (ПЕРЕЧИТАЙ ПЕРЕД ОТВЕТОМ)\n"  
        "══════════════════════════════════════\n"
        f"ТВОЯ ПОЗИЦИЯ (ты это сказал во вступлении, и ты ОБЯЗАН стоять на ней):\n"
        f"«{opponent_position}»\n\n"
        f"ПОЗИЦИЯ ОППОНЕНТА (ты ОБЯЗАН это АТАКОВАТЬ):\n"
        f"«{user_position}»\n"
        "══════════════════════════════════════\n"
    )

    instruction = (
        "───── ФАЗА 2: РАУНД ПОЛЕМИКИ ─────\n"
        ">> РЕЖИМ АТАКИ (ты задаёшь вопросы):\n\n"
        f"{anchor}\n"
        "ЗАДАЧА: Задай ОДИН короткий (20-25 слов) каверзный вопрос, "
        "который атакует позицию ОППОНЕНТА (антиинтеллектуализм).\n\n"
        "ВЫВЕДИ ТОЛЬКО ВОПРОС."
    )

    user_prompt = f"[ИНСТРУКЦИЯ]: {instruction}\n\n[ПОСЛЕДНЯЯ РЕПЛИКА ОППОНЕНТА]: {last_user_speech}"

    print("Начинаем тестирование...")
    for i in range(1, 6):
        start = time.time()
        # Используем обычную модель (reasoner), как запрашивалось, или можно передать use_chat_model=True для теста чата
        reply = deepseek.generate_opponent(system_prompt, user_prompt, stream=False)
        elapsed = time.time() - start
        
        print(f"Попытка {i}/5")
        print(f"Время: {elapsed:.1f} сек")
        print(f"Ответ: {reply}")
        print("---")

    deepseek.print_game_stats()

if __name__ == "__main__":
    main()
